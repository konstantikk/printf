#include "libft.h"

int		ft_atoi(const char *str)
{
	int f;
	int r;

	f = 1;
	r = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\f'
		|| *str == '\r' || *str == '\v')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			f = -1;
		if (*++str < '0' || *str > '9')
			return (0);
	}
	while (*str >= '0' && *str <= '9')
	{
		r += f * (*str++ - '0');
		if (*str >= '0' && *str <= '9')
			r *= 10;
	}
	return (r);
}
