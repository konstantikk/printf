#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char *r;

	if (!s1 || !s2)
		return (0);
	if (!(r = (char*)malloc(ft_strlen(s1) + ft_strlen(s2) + 1)))
		return (0);
	r = ft_strcpy(r, s1);
	return (ft_strcat(r, s2));
}
