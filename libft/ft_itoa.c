#include "libft.h"

char	*ft_itoa(int n)
{
	char	*r;
	int		i;
	int		k;

	i = 1;
	k = 1;
	while ((n / i <= -10 || n / i >= 10) && (i *= 10))
		k++;
	r = (n < 0) ? ((char*)ft_memalloc(k + 2)) : ((char*)ft_memalloc(k + 1));
	if (!n && (r[0] = '0'))
		return (r);
	if (!r)
		return (0);
	(n < 0) ? (r[0] = '-') : --k;
	i = (n < 0) ? (-1) : (1);
	while (n)
	{
		r[k--] = i * (n % 10) + '0';
		n /= 10;
	}
	return (r);
}
