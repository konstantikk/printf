#include "libft.h"

char	*ft_strdup(const char *s)
{
	char *r;

	if ((r = (char*)malloc((int)ft_strlen(s) + 1)))
		r = (char*)ft_memcpy(r, s, ft_strlen(s) + 1);
	else
		return (0);
	return (r);
}
