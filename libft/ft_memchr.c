#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned const char *helperstringer;

	helperstringer = s;
	while (n--)
		if (*helperstringer++ == (unsigned char)c)
			return ((unsigned char*)--helperstringer);
	return (0);
}
