#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*r;
	int		f;

	f = 0;
	r = dst;
	while (len--)
	{
		if (*src == '\0')
			f = 1;
		if (f)
			*dst = '\0';
		else
			*dst = *src++;
		dst++;
	}
	return (r);
}
