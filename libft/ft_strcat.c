#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *r;

	r = s1;
	while (*s1++)
		;
	while (*s2)
	{
		*(s1 - 1) = *s2++;
		s1++;
	}
	*(s1 - 1) = *s2;
	return (r);
}
